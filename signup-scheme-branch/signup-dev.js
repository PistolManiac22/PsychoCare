const slidePage = document.querySelector(".slide-page");
const nextBtnFirst = document.querySelector(".firstNext");
const prevBtnSec = document.querySelector(".prev-1");
const nextBtnSec = document.querySelector(".next-1");
const prevBtnThird = document.querySelector(".prev-2");
const nextBtnThird = document.querySelector(".next-2");
const prevBtnFourth = document.querySelector(".prev-3");
const submitBtn = document.querySelector(".submit");
const progressText = document.querySelectorAll(".step p");
const progressCheck = document.querySelectorAll(".step .check");
const bullet = document.querySelectorAll(".step .bullet");
let current = 1;

const account = {
  username,
  realname,
  email,
  institution,
  birth,
  gender,
  password,
}

account.username = document.querySelector('#username');
account.realname = document.querySelector('#name');
nextBtnFirst.addEventListener("click", (event) => {
  if (account.username && account.realname) {
    event.preventDefault();
    slidePage.style.marginLeft = "-25%";
    bullet[current - 1].classList.add("active");
    progressCheck[current - 1].classList.add("active");
    progressText[current - 1].classList.add("active");
    current += 1;
  } else if (!account.username) {
    alert('Masukkan Nama Pengguna!');
  } else if (!account.realname) {
    alert('Masukkan Nama!');
  } else {
    alert('Masukkan Nama Pengguna serta Nama Diri');
  }
});
